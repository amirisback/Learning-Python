# integer
xInt = int(1)   # x will be 1
yInt = int(2.8) # y will be 2
zInt = int("3") # z will be 3

# float
xFloat = float(1)     # x will be 1.0
yFloat = float(2.8)   # y will be 2.8
zFloat = float("3")   # z will be 3.0
wFloat = float("4.2") # w will be 4.2

# string
xString = str("s1") # x will be 's1'
yString = str(2)    # y will be '2'
zString = str(3.0)  # z will be '3.0'

print(xInt)
print(yInt)
print(zInt)

print(xFloat)
print(yFloat)
print(zFloat)
print(wFloat)

print(xString)
print(yString)
print(zString)